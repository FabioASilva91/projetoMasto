    public void exibirFoto(){  
        
          
        BufferedImage img = null;   
                                  
        try {  
            img = ImageIO.read(new ByteArrayInputStream(especie.getImage()));  
            jLFoto.setIcon(new ImageIcon(img));      
            ImageIO.write(img, "PNG", new File("h.png"));  
          
        } catch (IOException e) {  
            // TODO Auto-generated catch block  
            e.printStackTrace();  
        }  
} 
