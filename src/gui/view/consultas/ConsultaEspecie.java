    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        defaultTableModel.setRowCount(0);
        if (jTextFieldNomeEspecie.getText().trim().equals("")) {
            especies = new DaoEspecie().retrieveEspecie();
            BufferedImage img = null;   
            defaultTableModel.setColumnIdentifiers(new String[]{
                "id", "nome", "Familia", "tipo"});
            for (Especie especie1 : especies) {
                defaultTableModel.addRow(new Object[]{especie1.getId(), especie1.getNomeEspecie(), especie1.getFamilia().getNomeDaFamilia(), especie1.getTipoEspecie()});
                especie = especie1;
                
            }
                                  
            jTEspecies.setModel(defaultTableModel);
        } else {
            especies = new DaoEspecie().retrieveNomeEspecie(jTextFieldNomeEspecie.getText());
            for (Especie especie1 : especies) {
                defaultTableModel.addRow(new Object[]{especie1.getId(), especie1.getNomeEspecie(), especie1.getTipoEspecie()});
                if (!especie1.getTipoEspecie().equals("Etnozoologia")) {
                    especie = especie1;
                    exibirFoto();
                }
            }

        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

